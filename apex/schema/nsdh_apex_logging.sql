-- NS DemoHelper MVP APEX database logging.
-- Replace the admin password hash placeholder before applying in the APEX workspace.
-- This script creates flexible logging tables plus ORDS endpoints under:
--   /pls/apex/emeawj/nsdh

set define off

declare
  procedure run_ddl(p_sql in clob) is
  begin
    execute immediate p_sql;
  exception
    when others then
      if sqlcode not in (-955, -1408, -1430, -2260, -2275) then
        raise;
      end if;
  end;
begin
  run_ddl(q'[
    create table nsdh_sessions (
      id number generated by default on null as identity primary key,
      session_id varchar2(128) not null,
      anonymous_user_id varchar2(128),
      created_at timestamp with time zone default systimestamp not null,
      updated_at timestamp with time zone default systimestamp not null,
      app_version varchar2(64),
      environment varchar2(64),
      runtime_mode varchar2(64),
      source_page varchar2(128),
      browser_user_agent varchar2(1000),
      company_name varchar2(500),
      website varchar2(1000),
      audience_type varchar2(255),
      target_segment varchar2(255),
      industry varchar2(255),
      demo_strategy varchar2(255),
      discovery_score number,
      demo_readiness_score number,
      risk_score number,
      generation_status varchar2(64),
      error_message varchar2(4000),
      active_ai_provider varchar2(255),
      payload_json clob check (payload_json is json),
      constraint nsdh_sessions_sid_uk unique (session_id)
    )
  ]');

  run_ddl(q'[
    create table nsdh_session_inputs (
      id number generated by default on null as identity primary key,
      session_id varchar2(128) not null,
      created_at timestamp with time zone default systimestamp not null,
      source_action varchar2(255),
      payload_json clob check (payload_json is json)
    )
  ]');

  run_ddl(q'[
    create table nsdh_score_results (
      id number generated by default on null as identity primary key,
      session_id varchar2(128) not null,
      created_at timestamp with time zone default systimestamp not null,
      discovery_score number,
      demo_readiness_score number,
      risk_score number,
      gaps_json clob check (gaps_json is json),
      recommendations_json clob check (recommendations_json is json),
      payload_json clob check (payload_json is json)
    )
  ]');

  run_ddl(q'[
    create table nsdh_generated_outputs (
      id number generated by default on null as identity primary key,
      session_id varchar2(128) not null,
      created_at timestamp with time zone default systimestamp not null,
      output_type varchar2(128),
      generation_status varchar2(64),
      payload_json clob check (payload_json is json)
    )
  ]');

  run_ddl(q'[
    create table nsdh_event_log (
      id number generated by default on null as identity primary key,
      session_id varchar2(128),
      anonymous_user_id varchar2(128),
      created_at timestamp with time zone default systimestamp not null,
      source_page varchar2(128),
      action varchar2(255),
      status varchar2(64),
      error_code varchar2(128),
      error_message varchar2(4000),
      duration_ms number,
      payload_json clob check (payload_json is json)
    )
  ]');

  run_ddl(q'[
    create table nsdh_admin_settings (
      setting_name varchar2(128) primary key,
      setting_value varchar2(4000) not null,
      updated_at timestamp with time zone default systimestamp not null
    )
  ]');

  run_ddl(q'[
    create table nsdh_admin_tokens (
      token_hash varchar2(128) primary key,
      created_at timestamp with time zone default systimestamp not null,
      expires_at timestamp with time zone not null
    )
  ]');

  run_ddl('create index nsdh_sessions_updated_ix on nsdh_sessions(updated_at desc)');
  run_ddl('create index nsdh_sessions_company_ix on nsdh_sessions(company_name)');
  run_ddl('create index nsdh_inputs_session_ix on nsdh_session_inputs(session_id)');
  run_ddl('create index nsdh_scores_session_ix on nsdh_score_results(session_id)');
  run_ddl('create index nsdh_outputs_session_ix on nsdh_generated_outputs(session_id)');
  run_ddl('create index nsdh_events_session_ix on nsdh_event_log(session_id)');
end;
/

merge into nsdh_admin_settings s
using (
  select 'ADMIN_PASSWORD_SHA256' setting_name,
         '__ADMIN_PASSWORD_SHA256__' setting_value
  from dual
) src
on (s.setting_name = src.setting_name)
when matched then update set s.setting_value = src.setting_value, s.updated_at = systimestamp
when not matched then insert (setting_name, setting_value) values (src.setting_name, src.setting_value)
